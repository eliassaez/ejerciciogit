DROP DATABASE IF EXISTS BDD_practica_global_ILJ_elias_saez_1;
CREATE DATABASE BDD_practica_global_ILJ_elias_saez_1;
USE BDD_practica_global_ILJ_elias_saez_1;


DROP TABLE IF EXISTS MATRICULAS;
DROP TABLE IF EXISTS CENTRO_EDUCATIVOS;
DROP TABLE IF EXISTS ALUMNOS;
DROP TABLE IF EXISTS ESTUDIO;
DROP TABLE IF EXISTS ASIGNATURAS;


CREATE TABLE ASIGNATURAS (
	CODIGO_ASIGNATURA CHAR(5) NOT NULL,
	NOMBRE_ASIGNATURA VARCHAR(60) NOT NULL,
	DURACION_MODULO INT NOT NULL,
	CURSO_ASIGNATURA VARCHAR(30) NOT NULL,
 PRIMARY KEY (CODIGO_ASIGNATURA)) ENGINE = INNODB;

CREATE TABLE ESTUDIO (
	CODIGO_ESTUDIO CHAR(6) NOT NULL,
	NOMBRE_ESTUDIO VARCHAR(50) NOT NULL,
	ETAPA_ESTUDIO VARCHAR(20) NOT NULL,
 PRIMARY KEY (CODIGO_ESTUDIO)) ENGINE = INNODB;

CREATE TABLE ALUMNOS (
	DNI_ALUMNO CHAR(9) NOT NULL,
	NOMBRE_ALUMNO VARCHAR(30) NOT NULL,
	APELLIDO1_ALUMNO VARCHAR(35) NOT NULL,
	APELLIDO2_ALUMNO VARCHAR(35) NOT NULL,
	FECHA_NACIMIENTO_ALUMNO DATE NOT NULL,
	DIRECCION_ALUMNO CHAR(85) NOT NULL,
	TELEFONO_ALUMNO CHAR(9) NOT NULL,
 PRIMARY KEY (DNI_ALUMNO)) ENGINE = INNODB;

CREATE TABLE CENTRO_EDUCATIVOS (
	CODIGO_CENTRO CHAR(6) NOT NULL,
	NOMBRE_CENTRO VARCHAR(40) NOT NULL,
	DIRECCION_CENTRO VARCHAR(40) NOT NULL,
	TIPO_CENTRO CHAR(1) NOT NULL COMMENT 'P=PUBLICO
	C=CONCERTADO',
	NIVEL_EDUCATIVO_CENTRO CHAR(1) NOT NULL COMMENT 'P=PRIMARIA 
	S=SECUNDARIA,
	F=FORMACION PROFESIONAL,',
 PRIMARY KEY (CODIGO_CENTRO)) ENGINE = INNODB;

CREATE TABLE MATRICULAS (
	NUMERO_MATRICULA CHAR(10) NOT NULL,
	CODIGO_ASIGNATURA CHAR(5) NOT NULL,
	CODIGO_CENTRO CHAR(6) NOT NULL,
	CODIGO_ESTUDIO CHAR(6) NOT NULL,
	DNI_ALUMNO CHAR(9) NOT NULL,
	CURSO_ACADEMICO CHAR(9) NOT NULL,
	CALF_FINAL_ORDINARIA DECIMAL(2,0) NOT NULL,
	CALF_FINAL_EXTRAORDINARIA DECIMAL(2,0),
	NUMERO_CONVOCATORIA_ASIGNATURA CHAR(1) NOT NULL,
 PRIMARY KEY (NUMERO_MATRICULA)) ENGINE = INNODB;


ALTER TABLE MATRICULAS ADD FOREIGN KEY (CODIGO_ASIGNATURA) REFERENCES ASIGNATURAS (CODIGO_ASIGNATURA) ON DELETE  CASCADE ON UPDATE  CASCADE;
ALTER TABLE MATRICULAS ADD FOREIGN KEY (CODIGO_ESTUDIO) REFERENCES ESTUDIO (CODIGO_ESTUDIO) ON DELETE  CASCADE ON UPDATE  CASCADE;
ALTER TABLE MATRICULAS ADD FOREIGN KEY (DNI_ALUMNO) REFERENCES ALUMNOS (DNI_ALUMNO) ON DELETE  CASCADE ON UPDATE  CASCADE;
ALTER TABLE MATRICULAS ADD FOREIGN KEY (CODIGO_CENTRO) REFERENCES CENTRO_EDUCATIVOS (CODIGO_CENTRO) ON DELETE  CASCADE ON UPDATE  CASCADE;

